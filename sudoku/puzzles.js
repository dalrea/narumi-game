// 퍼즐 가져오기
function getPuzzle(level, index) {
    const puzzles = SUDOKU_PUZZLES[level];
    if (!puzzles || index < 0 || index >= puzzles.length) return null;
    return puzzles[index];
}

// 문자열을 2D 배열로 변환
function puzzleStringToGrid(str) {
    const grid = [];
    for (let r = 0; r < 9; r++) {
        grid[r] = [];
        for (let c = 0; c < 9; c++) {
            grid[r][c] = parseInt(str[r * 9 + c]);
        }
    }
    return grid;
}
